package callByMainWithReturnParameter;

public class Calculator {
	
	public int Add(int a, int b) {
		int c = a + b;
		
		return c;
	}
	
	public int Sub(int a, int b) {

		int c = a - b;
		
		return c;
	}
	
	public int Mul(int a, int b) {

		int c = a * b;
		
		return c;
	}
	
	public double Div(double a, double b) {

		double c = a / b ;
		
		return c;
	}

}
